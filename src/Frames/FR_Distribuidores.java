    private Boolean ValidacaoVazio(){
        if(JT_nome.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nome vazio", "Novo Cliente", WIDTH);
            return false;
        }else if(JT_razao_social.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Razão social vazio", "Novo Cliente", WIDTH);
            return false;
        }else if(JT_telefone.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Telefone vazio", "Novo Cliente", WIDTH);
            return false;
        }else if(JT_email.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Data de nascimento vazio", "Novo Cliente", WIDTH);
            return false;
        }
        return true;
    }

