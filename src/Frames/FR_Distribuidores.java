    private void BTN_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_salvarActionPerformed
        if(ValidacaoVazio()){            
            //populando Local
            Distribuidores dis = new Distribuidores();
            dis.salvarLocal(JT_nome.getText(), JT_razao_social.getText(), JT_telefone.getText(), JT_email.getText());
            
            //populando BD
            DAODistribuidores daoDistribuidores = new Distribuidores();
            daoDistribuidores.SalvarBD(dis);
            
            FR_Cadastrar cadastro = new FR_Cadastrar();
            cadastro.setVisible(true);
            setVisible(false);
        }
    }//GEN-LAST:event_BTN_salvarActionPerformed

