    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        JC_id_dist.removeAllItems();
        JC_id_item.removeAllItems();
        try {
            ConexaoBD conect = new ConexaoBD();
            Connection connection = conect.Conexao_banco();
            
            Statement sta = connection.createStatement();
            String str = "Select * from Distribuidores";
            ResultSet result = sta.executeQuery(str);
            while(result.next()){
                JC_id_dist.addItem(result.getInt(1)+" - "+result.getString(2));
            }
            
            str = "Select * from Itens_Venda";
            result = sta.executeQuery(str);
            while(result.next()){
                JC_id_item.addItem(result.getInt(1)+" - "+result.getString(3));
            }
            connection.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage(), "Popularização do JCombo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_formWindowActivated

