    private Boolean ValidacaoVazio(){
        if(JT_preco_venda.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Preço de venda vazio", "Novo Cliente", WIDTH);
            return false;
        }else if(JFT_data.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Data de nascimento vazio", "Novo Cliente", WIDTH);
            return false;
        }else if(JT_preco_custo.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Preco de custo vazio", "Novo Cliente", WIDTH);
            return false;
        }else if(JT_estoque.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nome do cliente vazio", "Novo Cliente", WIDTH);
            return false;
        }else if(JT_descricao.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nome do cliente vazio", "Novo Cliente", WIDTH);
            return false;
        }
        return true;
    }

