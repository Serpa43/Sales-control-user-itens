    private void BTN_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_salvarActionPerformed
        if(ValidacaoVazio()){
            String[] dia_mes = JFT_data.getText().split("/");
            String data = dia_mes[2] + "-" + dia_mes[1] +"-"+ dia_mes[0];
            
            Produtos prod = new Produtos();
            String[] ID_dis = JC_id_dist.getSelectedItem().toString().split(" ");
            String[] ID_item = JC_id_item.getSelectedItem().toString().split(" ");
            prod.salvarLocal(Double.parseDouble(JT_preco_venda.getText()), JT_descricao.getText(), data, Double.parseDouble(JT_preco_custo.getText()), Integer.parseInt(JT_estoque.getText()), Integer.parseInt(ID_item[0]), Integer.parseInt(ID_dis[0]));

            
            DAOProdutos produtos = new Produtos();
            produtos.SalvarBD(prod);
            
            FR_Cadastrar cadastro = new FR_Cadastrar();
            cadastro.setVisible(true);
            setVisible(false);
        }
    }//GEN-LAST:event_BTN_salvarActionPerformed

